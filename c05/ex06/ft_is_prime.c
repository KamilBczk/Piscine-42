int	ft_is_prime(int nb)
{
	int	result;
	int	i;

	i = 2;
	if (nb == 2)
		return (1);
	if (nb < 2)
		return (0);
	while (i != nb)
	{
		result = nb % i;
		if (result == 0)
			return (0);
		i++;
	}
	return (1);
}
