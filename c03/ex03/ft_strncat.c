char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int	i;
	int	f;

	f = 0;
	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while ((src[f] != '\0') && (nb != 0))
	{
		dest[i] = src[f];
		nb--;
		i++;
		f++;
	}
	dest[i] = '\0';
	return (dest);
}
